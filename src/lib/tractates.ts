// Helper to convert chapter index (0-based) to Hebrew letter (e.g. 0 -> 'א', 1 -> 'ב')
export function getHebrewChapter(num: number): string {
  const letters = ["א", "ב", "ג", "ד", "ה", "ו", "ז", "ח", "ט", "י", "יא", "יב", "יג", "יד", "טו", "טז", "יז", "יח", "יט", "כ", "כא", "כב", "כג", "כד", "כה", "כו", "כז", "כח", "כט", "ל"];
  return letters[num] || (num + 1).toString();
}
